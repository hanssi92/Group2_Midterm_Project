    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        CardSequencePanel.remove(this);
        ((java.awt.CardLayout) CardSequencePanel.getLayout()).previous(CardSequencePanel);

        // Find the ManageRecordsJPanel to call its refresh method
        for (java.awt.Component c : CardSequencePanel.getComponents()) {
            if (c instanceof ManageFaculty) {
                ((ManageFaculty) c).populateFacultyTable(); 
                break;
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String newEmail = txtEmail.getText();
        
        // Basic validation
        if(newEmail.isEmpty()) { // Updated variable and message
            JOptionPane.showMessageDialog(this, "Email cannot be empty.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Update the Person object
        // This updates the object held by FacultyProfile
        person.setEmail(newEmail);
        
        JOptionPane.showMessageDialog(this, "Faculty details saved successfully.", "Save Successful", JOptionPane.INFORMATION_MESSAGE);
        
        // Go back and refresh the table
        btnBackActionPerformed(evt);      
    }//GEN-LAST:event_btnSaveActionPerformed


