    private void populateDetails() {
        if (person != null) {
            txtFacultyId.setText(person.getPersonId());
            txtName.setText(person.getFirstName());
            txtEmail.setText(person.getEmail());
        }
        txtFacultyId.setEditable(false);
        txtName.setEditable(false);
            
            
            
        }
