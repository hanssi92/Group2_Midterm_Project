    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblHR.getSelectedRow();
        
        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblHR.getModel();
            selectedPerson = (Person) model.getValueAt(tblHR.convertRowIndexToModel(selectedRow), 0);
        } else {
            selectedPerson = null;
        }
        if (selectedPerson == null) {
            JOptionPane.showMessageDialog(this, "Please select a person from the table to delete.", "Selection Required", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirmation = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete person '" + selectedPerson.getFirstName() + "' (ID: " + selectedPerson.getPersonId() + ")?\nThis will also remove associated profiles and user accounts.", // Updated message
                "Confirm Deletion", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (confirmation == JOptionPane.YES_OPTION) {
            Department department = business.getDepartment();
            PersonDirectory personDirectory = department.getPersonDirectory();
            StudentDirectory studentDirectory = department.getStudentDirectory();
            FacultyDirectory facultyDirectory = department.getFacultyDirectory();
             // Assuming UserAccountDirectory is accessible via business object
            UserAccountDirectory userAccountDirectory = business.getUserAccountDirectory();

            String personIdToDelete = selectedPerson.getPersonId(); 
            
            boolean profileRemoved = false;
            boolean accountRemoved = false;
            boolean personRemoved = false;
            
            if (personRemoved) {
                JOptionPane.showMessageDialog(this, "Person deleted successfully.", "Deletion Success", JOptionPane.INFORMATION_MESSAGE);
                populateTable(); // Refresh the table
            } else {
                 JOptionPane.showMessageDialog(this, "Failed to delete person fully. Please check.", "Deletion Error", JOptionPane.ERROR_MESSAGE);
                 // Optionally repopulate even on partial failure to show current state
                 populateTable();
            }
        
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

